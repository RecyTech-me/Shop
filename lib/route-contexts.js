function createRouteContexts({
    app,
    infrastructure,
    domain,
    http,
}) {
    const {
        db,
        env,
        paymentProviders,
        repositories,
    } = infrastructure;
    const providers = {
        env,
        stripe: paymentProviders.stripe,
    };
    const {
        cart,
        checkout,
        formatters,
        forms,
        mail,
        money,
        payments,
        publicProducts,
        text,
        uploads,
        urls,
    } = domain;

    return {
        health: {
            app,
            db,
        },
        webhooks: {
            app,
            db,
            providers,
            repositories: repositories.orders,
            payments,
            text,
        },
        publicApi: {
            app,
            db,
            providers,
            http,
            text,
            publicProducts,
            cart,
            checkout,
            payments,
            products: repositories.products,
            orders: repositories.orders,
            mail,
        },
        storefront: {
            app,
            db,
            http,
            rateLimiters: {
                getReviewSubmissionRateLimitState: infrastructure.rateLimiters.getReviewSubmissionRateLimitState,
                registerReviewSubmissionAttempt: infrastructure.rateLimiters.registerReviewSubmissionAttempt,
            },
            text,
            money,
            forms,
            publicProducts,
            cart,
            products: repositories.products,
            reviews: repositories.reviews,
        },
        checkout: {
            app,
            db,
            providers,
            formatters,
            http,
            cart,
            checkout,
            forms,
            payments,
            orders: repositories.orders,
            mail,
        },
        admin: {
            session: {
                app,
                db,
                http,
                forms,
                admins: repositories.admins,
            },
            dashboard: {
                app,
                db,
                http,
                products: {
                    listAdminProductRows: repositories.products.listAdminProductRows,
                },
                reviews: repositories.reviews,
                dashboard: repositories.dashboard,
                orders: repositories.orders,
            },
            promoCodes: {
                app,
                db,
                http,
                forms,
                promos: repositories.promos,
            },
            orders: {
                app,
                db,
                http,
                text,
                money,
                forms,
                publicProducts,
                cart,
                checkout,
                urls,
                settings: repositories.settings,
                products: {
                    listAdminProducts: repositories.products.listAdminProducts,
                    getProductById: repositories.products.getProductById,
                },
                promos: repositories.promos,
                orders: repositories.orders,
                mail,
            },
            catalog: {
                app,
                db,
                http,
                text,
                uploads,
                settings: repositories.settings,
                products: {
                    createProduct: repositories.products.createProduct,
                    updateProduct: repositories.products.updateProduct,
                    deleteProduct: repositories.products.deleteProduct,
                    listPacksContainingProduct: repositories.products.listPacksContainingProduct,
                    listProductCategories: repositories.products.listProductCategories,
                    listAdminCategories: repositories.products.listAdminCategories,
                    deleteProductCategory: repositories.products.deleteProductCategory,
                    getProductById: repositories.products.getProductById,
                },
            },
        },
        fallback: {
            app,
            setFlash: http.setFlash,
            saveSessionAndRedirect: http.saveSessionAndRedirect,
            getSafeRedirectTarget: http.getSafeRedirectTarget,
        },
    };
}

module.exports = { createRouteContexts };
